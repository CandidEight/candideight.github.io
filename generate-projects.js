// Собираем projectsData
const projects = [];

for (let i = 1; i <= 24; i++) {
	const folderPath = path.join(portfolioPath, String(i));
	
	// Проверяем, есть ли папка
	if (!fs.existsSync(folderPath)) {
		console.log(`Папка ${i} не найдена, пропускаем`);
		continue;
	}
	
	// Читаем файлы в папке
	const files = fs.readdirSync(folderPath)
		.filter(file => /\.(jpg|jpeg|png|webp)$/i.test(file))
		.sort((a, b) => {
			// Сортируем по номеру (1.jpg, 2.jpg...)
			const numA = parseInt(a.match(/\d+/)?.[0] || 0);
			const numB = parseInt(b.match(/\d+/)?.[0] || 0);
			return numA - numB;
		});
	
	if (files.length === 0) {
		console.log(`В папке ${i} нет изображений`);
		continue;
	}
	
	// Главное фото (первое или 0.jpg если есть)
	const mainImageFile = files.includes('0.jpg') ? '0.jpg' : files[0];
	const mainImage = `/images/portfolio/${i}/${mainImageFile}`;
	
	// Остальные фото (исключаем главное если нужно)
	const otherFiles = files.filter(f => f !== mainImageFile);
	
	const images = otherFiles.map((file, index) => ({
		src: `/images/portfolio/${i}/${file}`,
		caption: `ПОДПИСЬ_К_ФОТО_${index + 1}`
	}));
	
	// Если нет других фото, добавляем хотя бы главное в карусель
	if (images.length === 0) {
		images.push({
			src: mainImage,
			caption: 'ПОДПИСЬ_К_ФОТО_1'
		});
	}
	
	projects.push({
		id: i,
		category: categories[i] || 'standard',
		title: titles[i-1] || `Проект ${i}`,
		shortDesc: shortDescs[i-1] || '',
		mainImage: mainImage,
		details: {
			fullDesc: fullDescs[i-1] || '',
			images: images,
			location: locations[i-1] || 'Самара',
			area: areas[i-1] || '0 м²',
			time: times[i-1] || '0 дней'
		}
	});
}

// Формируем содержимое файла
const fileContent = `const projectsData = ${JSON.stringify(projects, null, '\t')};`;

// Записываем в файл
fs.writeFileSync(outputFile, fileContent, 'utf8');

console.log(`✅ Сгенерирован файл с ${projects.length} проектами`);
console.log(`📁 Путь: ${outputFile}`);